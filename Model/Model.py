    mask = x==12975
    remask = torch.repeat_interleave(mask,seq_len, dim=0).view(-1,seq_len,seq_len).view(-1,seq_len,seq_len)

    for idx in remask:
        for i,iidx in enumerate(idx[0]):
            if iidx ==True:
                break
        idx[i:]=True

    remask2=torch.repeat_interleave(remask.unsqueeze(1), heads,dim=1)
    return remask2

